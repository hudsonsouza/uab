<br><br><br><br><p><span class="subtituloNormal">Justificativa

<br><br><p>É indiscutível o papel da educação a distância em atender demanda em várias área do conhecimento e do saber. A Ead amplia oferta, rompe com a distância e as barreiras geográficas, aproxima pessoas, cria oportunidades de um processo verdadeiro de inclusão  que possibilita  melhoria e qualidade de ensino.

<br><br><p>Contudo, muito ainda há por fazer. Os níveis dos alunos estão em ascensão nos países,  nos estados e porque não dizer nos municípios que levam a sério  projetos e ações, onde fica claro que a pobreza não precisa ser um determinante de desempenho educacional. 

<br><br><p>A educação que representa o futuro precisa oferecer coerência e experiência comum, junto com uma pratica diversificada, a uma atenção individual. Ela representa a versão educacional de  organização passando dos mercados de massas à produção especializada.

<br><br><br><br><p><span class="subtituloNormal">Esse futuro apresenta três grandes desafios. 

<br><br><p>Hoje a demanda da economia produtiva moderna e uma uma sociedade moderna leva o nosso sistema educacional a desenvolver o talento especial para cada individuo. Preparar cada membro para cumprir seu papel em uma sociedade complexa e rica em conhecimento.

<br><br><p>Em segundo lugar, as demandas dos jovens estão mudando. Pesquisa mostram que 70% dos jovens não estão  satisfeitos e  entediados. A explicação è de que o problema reside no  currículo. Existindo uma inadequação entre o que se sabemos que estimula o interesse dos jovens e a forma como ensinamos. Temos que inflamar a imaginação dos jovens e lhe proporcionar a alegria de aprender.

<br><br><p>Em terceiro lugar desafio de como ensinamos deve-se mostrar de forma vívida . Exige que o ensino no Séc XXI assuma não apenas a transmissão do conhecimento, mas também o aprendizado de como aprender, a ampliação do horizonte, e a qualificação dos professores. O que se vislumbra é todos os alunos desenvolvendo tarefas curriculares adequadamente desafiadoras, criada por professores capacitados. A medida que os alunos passam a dominar as informações e habilidade, passam também a enfrentar as futuras tarefas de aprendizagem, seja na escola e, seja fora dela.

<br><br><p>Estes são alguns desafios enfrentados por professores e estudantes na sociedade do conhecimento os quais não se resolvem com iniciativas de cima para baixo, e sim pelo cultivo de comunidades de aprendizagem, como proposta do Pólo EaD de Paranavaí.

<br><br><p>Vivemos numa sociedade e economia do conhecimento que são estimuladas  pela criatividade e inventividade, e as escolas da sociedades do conhecimento precisam gerar essas qualidades, caso contrario, seus alunos, seu povo  estarão fadado ao fracasso.

<br><br><p>Os profissionais da educação devem assumir novamente seus lugares de destaque entre os intelectuais mais respeitados da sociedade, indo além  do âmbito da sala de aula, para tornarem-se e prepararem seus alunos para serem cidadão do mundo.

<br><br><p>O Pólo EaD de Paranavaí como braço operacional da IES, assume também o compromisso para uma educação que floreça a criatividade, flexilidade, solução de problema,inventividade,inteligência coletiva, confiança profissional, disposição para os riscos, e aperfeiçoamento de todos.


<br><br><br><br><p><span class="subtituloNormal">Objetivo 

<br><br><p>Elevar em colaboração com as IES , a educação do Município de Paranavaí, na formação dos professores e comunidade, assim como na  formação continuada.

    
