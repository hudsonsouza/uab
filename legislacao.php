   <span class="subtituloNormal">Regulamentação da EAD no Brasil</span>
</center>

<br><br><p>No Brasil, as bases legais para a modalidade de educação a distância foram estabelecidas pela Lei de Diretrizes e Bases da Educação Nacional (Lei n.º 9.394, de 20 de dezembro de 1996), que foi regulamentada pelo Decreto  n.º 5.622, publicado no D.O.U. de 20/12/05 (que revogou o Decreto n.º 2.494, de 10 de fevereiro de 1998, e o Decreto n.º 2.561, de 27 de abril de 1998) com normatização definida na Portaria Ministerial  n.º 4.361, de 2004 (que revogou a Portaria Ministerial n.º 301, de 07 de abril de 1998 ). 

<br><br><p>Em 3 de abril de 2001, a Resolução n.º 1, do Conselho Nacional de Educação estabeleceu as normas para a pós graduação lato e stricto sensu.   

<br><br><br><br><p><span class="subtituloNormal">A. EDUCAÇÃO BÁSICA na modalidade de Educação a Distância:</span>

<br><br><p>De acordo com o Art. 30º do Decreto n.º 5.622/05, "As instituições credenciadas para a oferta de educação a distância poderão solicitar autorização, junto aos órgãos normativos dos respectivos sistemas de ensino, para oferecer os ensinos fundamental e médio a distância, conforme § 4o do art. 32 da Lei no 9.394, de 1996, exclusivamente para:

<br><br><p>I - a complementação de aprendizagem; ou 
<br><br><p>II - em situações emergenciais.

<br><br><p>Para oferta de cursos a distância dirigidos à educação fundamental de jovens e adultos, ensino médio e educação profissional de nível técnico, o Decreto n.º 5.622/05 delegou competência às autoridades integrantes dos sistemas de ensino de que trata o artigo 8º da LDB, para promover os atos de credenciamento de instituições localizadas no âmbito de suas respectivas atribuições.

<br><br><p>Assim, as propostas de cursos nesses níveis deverão ser encaminhadas ao órgão do sistema municipal ou estadual responsável pelo credenciamento de instituições e autorização de cursos (Conselhos Estaduais de Educação)  a menos que se trate de instituição vinculada ao sistema federal de ensino, quando, então, o credenciamento deverá ser feito pelo Ministério da Educação.



<br><br><br><br><p><span class="subtituloNormal">B. EDUCAÇÃO SUPERIOR e EDUCAÇÃO PROFISSIONAL  na modalidade de Educação a Distância:</span>

<br><br><p>No caso da oferta de cursos de graduação e educação profissional em nível tecnológico, a instituição interessada deve credenciar-se junto ao Ministério da Educação, solicitando, para isto, a autorização de funcionamento para cada curso que pretenda oferecer. O processo será analisado na Secretaria de Educação Superior, por uma Comissão de Especialistas na área do curso em questão e por especialistas em educação a distância. O Parecer dessa Comissão será encaminhado ao Conselho Nacional de Educação. O trâmite, portanto, é o mesmo aplicável aos cursos presenciais. A qualidade do projeto da instituição será o foco principal da análise. Para orientar a elaboração de um projeto de curso de graduação a distância, a Secretaria de Educação a Distância elaborou o documento Indicadores de qualidade para cursos de graduação a distância, disponível no site do Ministério para consulta. As bases legais são as indicadas no primeiro parágrafo deste texto. 



<br><br><br><br><p><span class="subtituloNormal">C. Pós-graduação a distância</span>  

<br><br><p>A possibilidade de cursos de mestrado, doutorado e especialização a distância foi disciplinada pelo Capítulo V  do Decreto n.º 5.622/05 e pela Resolução nº 01, da Câmara de Ensino Superior-CES, do Conselho Nacional de Educação-CNE, em 3 de abril de 2001. 

<br><br><p>O artigo 24 do Decreto n.º 5.622/05, tendo em vista o disposto no § 1º do artigo 80 da Lei nº 9.394, de 1996, determina que os cursos de pós-graduação stricto sensu (mestrado e doutorado) a distância serão oferecidos exclusivamente por instituições credenciadas para tal fim pela União e obedecem às exigências de autorização, reconhecimento e renovação de reconhecimento estabelecidos no referido Decreto. 

<br><br><p>No artigo 11, a Resolução nº 1, de 2001, também conforme o disposto no § 1º do art. 80 da Lei nº 9.394/96, de 1996, estabelece que os cursos de pós-graduação lato sensu a distância só poderão ser oferecidos por instituições credenciadas pela União. 

<br><br><p>Os cursos de pós-graduação lato sensu oferecidos a distância deverão incluir, necessariamente, provas presenciais e defesa presencial de monografia ou trabalho de conclusão de curso. 



<br><br><br><br><p><span class="subtituloNormal">D. Diplomas e certificados de cursos a distância emitidos por instituições estrangeiras </span>

<br><br><p>Conforme o Art. 6º do Dec. 5.622/05, os convênios e os acordos de cooperação celebrados para fins de oferta de cursos ou programas a distância entre instituições de ensino brasileiras, devidamente credenciadas, e suas similares estrangeiras, deverão ser previamente submetidos à análise e homologação pelo órgão normativo do respectivo sistema de ensino, para que os diplomas e certificados emitidos tenham validade nacional. 

<br><br><p>A Resolução CES/CNE 01, de 3 de abril de 2001, relativa a cursos de pós-graduação, dispõe, no artigo 4º, que os diplomas de conclusão de cursos de pós-graduação stricto sensu obtidos de instituições de ensino superior estrangeiras, para terem validade nacional, devem ser reconhecidos e registrados por universidades brasileiras que possuam cursos de pós-graduação reconhecidos e avaliados na mesma área de conhecimento e em nível equivalente ou superior ou em área afim. 

<br><br><p>Vale ressaltar que a Resolução CES/CNE nº 2, de 3 de abril de 2001, determina no caput do artigo 1º, que os cursos de pós-graduação stricto sensu oferecidos no Brasil por instituições estrangeiras, diretamente ou mediante convênio com instituições nacionais, deverão imediatamente cessar o processo de admissão de novos alunos. 

<br><br><p>Estabelece, ainda, que essas instituições estrangeiras deverão, no prazo de 90 (noventa) dias, a contar da data de homologação da Resolução, encaminhar à Fundação Coordenação de Aperfeiçoamento de Pessoal de Nível Superior  CAPES a relação dos diplomados nesses cursos, bem como dos alunos matriculados, com a previsão do prazo de conclusão. Os diplomados nos referidos cursos deverão encaminhar documentação necessária para o processo de reconhecimento por intermédio da CAPES.



