<br><br><p>A expectativa do adequado exercício da função de coordenador de pólo prevê, dentre outras atribuições obrigatórias, as que se seguem:

<br><br>- Representar o município/estado junto ao MEC e instituições de ensino superior, em relação às ações desenvolvidas no âmbito da Universidade Aberta do Brasil;
<br><br>- Mediar a comunicação do município/estado com o MEC;
<br><br>- Preencher o Cadastro de Professores da Educação básica no município, permitindo a filiação dos mesmos ao pólo de apoio presencial, a partir dos dados do INEP e sistema de cadastro eletrônico a ser disponivilizado pelo MEC;
<br><br>- Participar das reuniões, encontros e eventos relativos ao Sistema UAB;
<br>- Coordenar a articulação e comunicação com os partícipes do Sistema Universidade Aberta do Brasil, destacadamente a Coordenação da UAB no MEC;
<br><br>- Coordenar a implantação dos projetos e ações no âmbito do Pólo de apio presencial, bem como o contato com as IES que atuam no pólo;
<br><br>- Participar do Fórum on-line dos coordenadores de pólos de apoio presencial, a ser instituído e coordenado pelo MEC, com espaço oficial das discussões, troca de idéias e informações sobre o Sistema UAB;
<br><br>- Criar mecanismos de articulação junto às IES, mantendo a comunicação com os coordenadores da UAB dessas instituições;
<br><br>- Acompanhar e apoiar a execução das atividades pedagógicas dos cursos ofertados nos Pólos de apoio presencial pelas IES, garantindo condições técnicas, operacionais e administrativas adequadas;
<br><br>- Relatar ao MEC, periodicamente, fatos ocorridos e relevantes para o adequado funcionamento do Sistema UAB;
<br><br>- Realizar reuniões periódicas com o corpo técnico do Pólo e equipe pedagógica das IES para a avaliação do Sistema UAB a fim de promover e manter a qualidade dos cursos e traçar estratégias para a melhoria dos serviços oferecidos à população;
<br><br>- Zelar pelo bom funcionamento do pólo, promovendo a solução de eventuais irregularidades e dificuldades junto aos responsáveis;
<br><br>- Criar endereço de e-mail específico para a comunicação com o MEC e IES, nos termos definidos pela Secretria de Educação a Distância.
