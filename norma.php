   <span class="subtituloNormal">NORMAS E REGULAMENTOS DO PÓLO UAB-PARANAVAÍ</span>
</center>
<br><br><p>A partir do dia 1º de Outubro de 2007, passará a funcionar o Pólo Presencial de Apoio ao Ensino Superior a Distância de Paranavaí.

<br><br><p>Casa da Cultura – Todo encontro previsto neste local, será informado com antecedência, via e-mail, através do fórum de alunos;

<br><br>- Cada grupo de 25 (vinte e cinco) alunos, ficará sob a responsabilidade de um Tutor para melhor acompanhamento do desempenho e auxílio quando necessário. Embora cada grupo de 25 (vinte e cinco) alunos tenha um Tutor responsável, não tirará a responsabilidade e o direito dos alunos de buscar informações com qualquer outro Tutor (a);
<br><br>- Solicitamos a todos a responsabilidade e cuidado ao utilizarem os computadores. O uso dos computadores é EXCLUSIVAMENTE para estudo.
<br><br>- Não será permitida a entrada nos espaços: laboratório de informática, biblioteca, sem o crachá de identificação do aluno UAB, bem como a entrada no laboratório de pessoas que não sejam alunos UAB;
<br><br>- Avaliação presencial: Não será permitido atraso. No caso de ausência, somente mediante atestado médico protocolado junto à Secretaria, no prazo de 72 (setenta e duas) horas, sendo que este será encaminhado a UTFPR para análise do requerimento;
<br><br>- Laboratório de informática e biblioteca - Não serão permitidas manifestações que prejudiquem os trabalhos;
<br><br>- Adquira o hábito de abrir seu e-mail diariamente. TODAS as informações serão transmitidas via correio eletrônico (e-mail);

