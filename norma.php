   <span class="subtituloNormal">REGRAS PARA USO DA PLATAFORMA MOODLE</span>
</center>
<br><br>1. Mensagens e tópicos relacionados à pirataria (cracks, seriais, downloads de programas (jogos completos, apologia, etc), códigos destrutivos e/ou maliciosos, informações sigilosas e/ou pessoais, e similares serão imediatamente excluídos e o participante que postou poderá ser banido por uma semana ou expulso do Fórum. 

<br><br>2. Não é permitido ofender, provocar, ameaçar ou ironizar nenhum participante e é proibido o uso de palavras de baixo calão nas mensagens e assinaturas, sob pena de expulsão imediata. O mesmo se aplica às mensagens privadas. Críticas e reclamações sobre o comportamento de outros participantes devem ser evitadas! Se desejar reclamar sobre o comportamento de um outro participante envie um e-mail para um dos Administradores do Fórum ead@utfpr.edu.br com as suas reclamações e links que as fundamentem. 

<br><br>2.a. Não é permitida a intromissão em tópicos de compra-venda com a intenção de depreciar ou atrapalhar a compra e/ou venda. O participante poderá ser suspenso do Fórum. 

<br><br>2.b. Não é permitida a postagem de mensagens com provocação a qualquer produto ou software; o participante que fizer isso será expulso do Fórum. 

<br><br>3. É proibido postar imagens pornográficas ou com conteúdo que fere as demais regras. 

<br><br>4. Mensagens duplicadas (ou enviada mais de 2 vezes) terão as cópias excluídas, e o participante poderá ser suspenso caso o ato tenha sido intencional. 

<br><br>5. Ao postar informações provenientes de qualquer outro site, é obrigatória a publicação da fonte do mesmo, o nome do autor e o link para o artigo original. O tópico será excluído caso alguma informação esteja incompleta. 

<br><br>6. As discussões que se tornarem pessoais (com ataques, ofensas ou ameaças) serão imediatamente apagadas e os envolvidos expulsos. 

<br><br>7. Não é permitido postar o e-mail pessoal com o objetivo de receber ou oferecer qualquer auxílio que poderia ser feito pelo Fórum. O Fórum foi criado para intermediar a comunicação entre os participantes, área. 

<br><br>8. O uso de letras maiúsculas, em negrito ou com fonte maior do que 2 (dois) é proibida na postagem de mensagens inteiras: o seu uso só é permitido em trechos de mensagens visando chamar a atenção para a informação ali contida. Também é proibida a postagem de mensagens inteiras com cores berrantes (vermelho, rosa ...) ou qualquer outra maneira de chamar a atenção. 

<br><br>9. Não serão permitidas críticas das regras já definidas (avatares, mudança de nicks, selos, nº de posts, etc), os tópicos sobre esses assuntos serão apagados. 

<br><br>10. O fórum não é lugar para discussões pessoais. Iremos expulsar os usuários que ficarem trazendo problemas pessoais para o Fórum. 

<br><br>11. "Tópicos de despedida" serão apagados, pois se alguém quiser sair do Fórum não precisa ficar fazendo discurso em público. 

<br><br>12. Fórum é Fórum; Chat é Chat. Tópicos ou mensagens que se transformarem em chat poderão ser apagados, pois o objetivo do fórum é ter conteúdo técnico. 

<br><br>13. Discutir em tópicos sobre as decisões tomadas pelos moderadores e administradores poderá gerar a suspensão do login. Caso não esteja satisfeito com alguma decisão tomada entre em contato com o administrador e-mail ead@utfpr.edu.br. 

<br><br>14. As regras do Fórum poderão ser alteradas sem prévio aviso. 

<br><br>15. O participante que for suspenso, e já tiver histórico de suspensão, será banido em definitivo. 

<br><br>16. Sempre que se fizer necessário, os Administradores poderão fechar áreas ou tomar quaisquer atitudes drásticas visando manter o bom andamento do Fórum. 

<br><br>17. Qualquer situação não prevista nas regras, mas que gerar confusão, conflito, brigas e/ou discussões desnecessárias, bem como demais situações que prejudiquem o fórum e seus participantes, será rigorosamente observada pelos moderadores e administradores. Caso, utilizando o bom-senso, o moderador/administrador julgar que para o bem de todos a mensagem deverá ser trancada ou excluída, ele tem total liberdade para fazê-lo. Posteriormente, se necessário, as regras serão editadas. 

