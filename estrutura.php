<div class="titulo">
TUTORIA PRESENCIAL NOS PÓLOS</div>

<br><br>
<div class="titulo">
OBJETIVO</div>

<br><br><p>A Tutoria presencial nos pólos tem como objetivo a criação de ambientes de trabalho que permitam o atendimento individualizado dos alunos, que possibilitem a organização desses alunos em grupos promovendo o trabalho cooperativo e colaborativo e estimulem o aluno a expor suas dúvidas relacionadas tanto sobre seu entendimento sobre conteúdo da matéria, quanto a dificuldades de outra ordem que estejam dificultando o seu desenvolvimento no curso. 

<br><br><br>
<div class="titulo">
ESTRUTURA</div>

<br><br><p>A tutoria presencial é realizada nos Pólos por uma equipe de tutores que é constituída por profissionais com nível superior e é organizada por área de conhecimento. A cada área corresponde uma equipe de tutores que desenvolve os seus trabalhos, por disciplina, sob a orientação do tutor-coordenador da área respectiva. 
<br><br><p>O espaço em que se dá a tutoria presencial consiste de toda infra-estrutura administrativa e acadêmica necessária para o desenvolvimento dos trabalhos dos tutores: salas de aula, salas de estudo, bibliotecas, laboratórios necessários à parte prática das disciplinas e salas para seminários.
Os alunos são permanentemente assessorados e acompanhados por professores (tutores) cujo leque de atribuições é extremamente variado e importante. Neste contexto, o estimulo ao processo de aprendizagem, o incentivo ao desenvolvimento do estudo, a participação nas discussões em fóruns, salas de bate-papo, encontros presenciais e estruturação de avaliações são algumas de suas atribuições mais importantes. 
<br><br><p>Seja utilizando o e-mail ou as ferramentas de comunicação síncronas e assíncronas disponibilizadas no ambiente virtual de aprendizagem, o aluno é permanentemente acompanhado e avaliado, seja em atividades formais ou através de suas participações.
