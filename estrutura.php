<div class="titulo">
PROCESSO SELETIVO</div>

<br><br><p>O processo de seleção dos tutores dos cursos do CEDERJ foi planejado de modo criterioso para que sejam selecionados profissionais capazes de desenvolver junto aos alunos uma tutoria responsável que seja adequada à formação de profissionais qualificados. Nas universidades a seleção dos tutores e professores tutores se realiza a partir de uma ampla divulgação, análise de currículo e entrevista, cujos critérios são definidos de acordo com a coordenação de curso e coordenações de disciplina. Os tutores que atuam nos pólos regionais passam por um processo seletivo que ocorre periodicamente para todos os pólos. 
<br><br><p>É organizado pela coordenação de tutoria do CEDERJ e consiste de duas etapas, a primeira contendo duas fases, a)uma prova escrita e b) entrevista e análise de currículo e a segunda etapa que consiste de uma capacitação que se divide em capacitação inicial em conteúdo e em EAD. 

<br><br><br>
<div class="titulo">
CONSIDERAÇÕES FINAIS</div>

<br><br><p>O modelo de tutoria descrito está sendo implementado e constantemente avaliado para que seja uma estrutura capaz de propiciar a interação e interatividade necessária para o cumprimento dos objetivos educacionais dos projetos didáticos-pedagógicos dos cursos. 
<br><br><p>A seleção criteriosa dos tutores e a sua capacitação são consideradas fatores decisivos para a implementação dos cursos. Os tutores são responsáveis pela adoção de estratégias que auxiliam o aluno a refletir sobre o seu aprendizado, a assumir para ele próprio a responsabilidade pelo seu desenvolvimento no curso e a adquirir autonomia de estudo. O profissional para atuar como tutor deve estar preparado para assumir diversas tarefas que se resumem em um conjunto de ações que motivem os alunos na continuidade e na finalização de seus estudos.

