<div class="titulo">
TUTORIA A DISTÂNCIA NAS UNIVERSIDADES CONSORCIADAS</div>


<br>
<div class="titulo">
OBJETIVO</div>

<br><br><p>A Tutoria a distância tem como objetivo orientar os alunos em suas dúvidas, de forma rápida e objetiva, por meio de comunicação individual, fax, telefone e pela utilização de ferramentas do ambiente on-line tais como: correio eletrônico, tira dúvidas, fórum e chats, estimulando-os a criar grupos de estudo para o trabalho cooperativo e colaborativo.
<br><br><p>Estrutura 
<br><br><p>A tutoria a distância é realizada nas Universidades Consorciadas por meio do atendimento a distância, por disciplina. A equipe de tutoria poderá se constituir, de acordo com procedimentos definidos pela coordenação do curso, de alunos de pós-graduação, graduação; de professores das universidades consorciadas e de profissionais graduados na área com ou sem vínculo nas universidades consorciadas. Esta equipe é coordenada por um professor da universidade que oferece a disciplina ¬ denominado de Coordenador da Disciplina ¬ que é responsável pelos trabalhos de tutoria presencial e a distância da respectiva disciplina. De acordo com o aumento do número de alunos por tutor, o coordenador da disciplina indicará a necessidade de incluir professores tutores para responder pelo acompanhamento dos trabalhos dos tutores da disciplina. 

