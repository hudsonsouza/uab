<br><br><p>Conhecer o projeto didático-pedagógico do curso e o material didático das disciplinas sob sua responsabilidade, demonstrando domínio do conteúdo específico da área. 
<br><br><p>Orientar o aluno para o estudo a distância, buscando mostrar a necessidade de se adquirir autonomia de aprendizagem. 
<br><br><p>Orientar o aluno, individualmente ou em grupo, visando a orientá-lo para a construção de uma metodologia própria de estudo. 
<br><br><p>Indicar ao aluno a necessidade de pesquisar a bibliografia sugerida no material didático, no sentido do aprofundamento dos conteúdos das disciplinas. 
<br><br><p>Participar de encontros, atividades culturais, videoconferências e seminários presenciais programados pela coordenação do curso, desde que programadas com antecedência. 
<br><br><p>Emitir relatório semanal para o professor tutor da disciplina, com o registro da participação do aluno, suas principais dúvidas e respectivas orientações e encaminhamentos e registro de informações sobre os tipos e os níveis de dificuldades que os alunos apresentam em relação a tópicos das disciplinas e respectivo material didático. 
<br><br><p>Cumprir com pontualidade os horários de atendimento aos alunos de acordo com o estabelecido pela coordenação de tutoria do CEDERJ. 
Atribuições do professor-tutor:
<br><br><p>Orientar a equipe de tutores da disciplina nos trabalhos da tutoria a distância. 
<br><br><p>Acompanhar o processo de tutoria relativo à disciplina ou conjunto de disciplinas sob sua responsabilidade, estabelecendo uma tabela de atendimento a distância dos alunos. 
<br><br><p>Organizar, em conjunto com o professor coordenador da disciplina, o seu processo de avaliação. 
<br><br><p>Corrigir e revisar provas, trabalhos e outras atividades a distância, que compõem o sistema de avaliação da aprendizagem, sob a coordenação do professor coordenador da disciplina. 
<br><br><p>Definir junto ao coordenador da disciplina horários de atendimento dos tutores de acordo com a programação da coordenação do curso. 
Atribuições do professor coordenador da disciplina
<br><br><p>Coordenar os trabalhos da tutoria presencial e a distância relativa à disciplina sob sua orientação. 
<br><br><p>Participar do processo de seleção de tutores presenciais e a distância da área organizando a capacitação dos tutores no conteúdo da disciplina, com base no material didático. 
<br><br><p>Organizar em conjunto com a coordenação do curso palestras, seminários nos pólos destinados a alunos e tutores. 
<br><br><p>Realizar visitas periódicas aos pólos para reuniões de acompanhamento e avaliação com tutores nos pólos. 
<br><br><p>Coordenar equipes de professores tutores da universidade na elaboração do conteúdo das provas e programação de trabalhos e atividades presenciais e a distância que compõem o sistema de avaliação da aprendizagem definido pela coordenação do curso. 
<br><br><p>Coordenar equipes de professores tutores da universidade na correção e revisão de provas, trabalhos e outras atividades presenciais e a distância que compõem o sistema de avaliação da aprendizagem definido pela coordenação do curso. 
<br><br><p>Estar em permanente contato com o tutor coordenador do Pólo para acompanhamento e orientação sobre as atividades da tutoria relativas às disciplinas sob sua responsabilidade. 


