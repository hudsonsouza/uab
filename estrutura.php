Pólos ou Sedes Tutorais</div>


<br><br><p>São os âmbitos instituicionais onde acontecem as tutorias. Suas funções e responsabilidades são as seguintes:

  <br>
  <br>

<ul>
  <li> Colocar à disposição dos tutores e dos estudantes os equipamentos e a infra-estrutura necessária para que se possam levar adiante as tutorias. <br>
  </li>
  <li> Promover e dar publicidade à matrícula dos destinatários e sua área de influência. <br>
  </li>
  <li> Realizar o acompanhamento e a avaliação das diferentes etapas. <br>
  </li>
  <li> Administrar a informação acadêmica e administrativa correspondente aos alunos ligados a determinada Universidade. <br>
  </li>
  <li> Dar apoio político e operacional para o desenvolvimento. <br>
  </li>
  <li> Assegurar e facilitar a participação institucional da equipe em todos os eventos de capacitação, assessoramento e avaliação determinados. <br>
    <br>
    <br>
  </li>
</ul>
<p>Perfil do recurso humano do pólo:<br>
<br>

<ul>
  <li> Equipe diretiva com capacidade acadêmica e gerencial para elevar adiante a proposta de capacitação desta modalidade. <br>
  </li>
  <li> Equipe de tutores avaliados em sua capacidade acadêmica. <br>
  </li>
  <li> Pessoal administrativo para completar os requerimentos de matrícula. <br>
    <br>
    <br>
  </li>
</ul>
